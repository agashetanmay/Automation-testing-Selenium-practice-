package seleniumTutorial;

public class programme1 {

	public static void main(String[] args) {
		
		String input = "selenium";
		
		String search = "a";
		
		int count =0;
		
		for (int i=0;i<input.length();i++);
				
			
			if (input.charAt(i) == search) {
				
				
				count++;
			}
				
				System.out.println(count);
	}
}
